    void CentrarVentana(JInternalFrame frame) {
//EL formulario ventas se agrega dentro de la ventana principal
        VentanaPrincipal.add(frame);
        //se necesita una variable de dimension para sacar su dimension por eso se crean dos
        Dimension dimension= VentanaPrincipal.getSize();
        Dimension Dframe=frame.getSize();//hace referencia al frme que queremos en este caso frame ventas 
        frame.setLocation((dimension.width -Dframe.height)/2, (dimension.height-Dframe.width)/2);//codigo para centrar
        frame.show();//se muestra centrado
        

    }

